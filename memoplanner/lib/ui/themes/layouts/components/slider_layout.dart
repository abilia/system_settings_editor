class SliderLayout {
  final double defaultHeight,
      leftPadding,
      rightPadding,
      iconRightPadding,
      thumbRadius,
      elevation,
      pressedElevation,
      outerBorder,
      trackHeight;

  const SliderLayout({
    this.defaultHeight = 56,
    this.leftPadding = 12,
    this.rightPadding = 4,
    this.iconRightPadding = 12,
    this.thumbRadius = 12,
    this.elevation = 1,
    this.pressedElevation = 2,
    this.outerBorder = 2,
    this.trackHeight = 4,
  });
}
